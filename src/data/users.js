/**
 * Recherche un utilisateur par email OU username + mot de passe.
 * @param {string} identifier  - email ou username saisi
 * @param {string} password    - mot de passe en clair
 * @returns {object|null}      - l'utilisateur trouvé (sans le mot de passe) ou null
 */
export function findUser(identifier, password) {
  const normalized = identifier.trim().toLowerCase();

  const user = USERS.find(
    (u) =>
      (u.email.toLowerCase() === normalized ||
        u.username.toLowerCase() === normalized) &&
      u.password === password &&
      u.statut === "actif"
  );

  if (!user) return null;

  // ⚠️  On ne renvoie jamais le mot de passe vers le reste de l'app
  const { password: _pwd, ...safeUser } = user;
  return safeUser;
}
